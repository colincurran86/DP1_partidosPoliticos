package models;

public class PersonaReniec {
	private String nombre;
	private String apellidos;
	private String dni;
	private int ubigeo;
	private int idHuella;
	private String idFirma;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public int getUbigeo() {
		return ubigeo;
	}
	public void setUbigeo(int ubigeo) {
		this.ubigeo = ubigeo;
	}
	public int getIdHuella() {
		return idHuella;
	}
	public void setIdHuella(int idHuella) {
		this.idHuella = idHuella;
	}
	public String getIdFirma() {
		return idFirma;
	}
	public void setIdFirma(String idFirma) {
		this.idFirma = idFirma;
	}
	
	
}
